# Variavel é um recurso o qual utilizamos em programas para armazenar
# dados e servi-los ao processador, e também é um espaço na memória o 
# qual reservamos e damos um nome. A diferença entre variável e constante
# esrá na quantidade de vezes em que os dados podem ser alterados, as constantes
# são predefinidas, ou seja, não podem alterar o seu valor. Já as variáveis podem ter
# seu valor alterado ao longo da execução do programa. As variáveis e constantes sãp
# muito importantes para o programador, são elas que fazem o programa executar e possuír 
# algum sentido e utilidade. 